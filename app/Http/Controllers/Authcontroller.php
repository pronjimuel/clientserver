        //
        $path = public_path().'/images/products/'. $request->name . ".png";
        $current = file_get_contents($request->file('fileToUpload'));

        file_put_contents($path,$current);
        User::create(['image' => $path,'username' => $request->name,'storename' => $request->location,'location' => $request->category, 'email' => $request->email, 'password' => $request->password])
    }

